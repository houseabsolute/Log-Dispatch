		       'Dispatch/Base.pm' => '$(INST_LIBDIR)/Dispatch/Base.pm',
		       'Dispatch/Output.pm' => '$(INST_LIBDIR)/Dispatch/Output.pm',
		       'Dispatch/File.pm' => '$(INST_LIBDIR)/Dispatch/File.pm',
